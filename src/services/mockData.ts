export function generateMockHistory(): PrintHistoryEntry[] {
  const entries: PrintHistoryEntry[] = [];
  const printers = mockPrinters;
  for (let i = 0; i < 50; i++) {
    const printer = printers[Math.floor(Math.random() * printers.length)];
    const daysAgo = Math.floor(Math.random() * 30);
    const isSuccess = Math.random() > 0.15;
    entries.push({
      id: `h-${i}`,
      printer_id: printer.id,
      printer_name: printer.name,
      timestamp: new Date(Date.now() - daysAgo * 86400000 - Math.random() * 86400000).toISOString(),
      result: isSuccess ? "success" : "error",
      message: isSuccess ? "Self-test print completed" : "Connection timeout",
    });
  }
  return entries.sort((a, b) => new Date(b.timestamp).getTime() - new Date(a.timestamp).getTime());
}

